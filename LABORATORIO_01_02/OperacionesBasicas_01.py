#Ejercicio 01
#Realiza la suma, resta, multiplicación y división de dos números ingresados por el usuario.

# Solicitar al usuario que ingrese dos números
num1 = float(input("Ingresa el primer número: "))
num2 = float(input("Ingresa el segundo número: "))

# Realizar las operaciones
suma = num1 + num2
resta = num1 - num2
producto = num1 * num2
division = num1 / num2

# Imprimir los resultados
print("La suma de", num1, "+", num2, "es:", suma)
print("La resta de", num1, "-", num2, "es:", resta)
print("El producto de", num1, "*", num2, "es:", producto)
print("La división de", num1, "/", num2, "es:", division)

