#Área de un Triángulo:
#3) Pide la base y la altura de un triángulo al usuario y calcula su área.

# Solicitar al usuario que ingrese la base y la altura del triángulo
base = float(input("Ingresa la base del triángulo: "))
altura = float(input("Ingresa la altura del triángulo: "))

# Calcular el área del triángulo
area = 0.5 * base * altura

# Imprimir el área del triángulo
print("El área del triángulo con base", base, "y altura", altura, "es:", area)