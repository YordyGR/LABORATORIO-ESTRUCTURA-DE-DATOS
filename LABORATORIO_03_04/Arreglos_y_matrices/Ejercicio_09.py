#9) Accede al elemento central de una matriz.

# Crear una matriz de 3x3
matriz = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
# Obtener el elemento central de la matriz
elemento_central = matriz[len(matriz) // 2][len(matriz[0]) // 2]
# Imprimir el elemento central
print("El elemento central de la matriz es:", elemento_central)